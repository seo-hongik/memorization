//패키지 이름이 classTest입니다.
package classTest;

//Student 클래스를 정의합니다.
public class Student {
	
	//필드 (멤버변수)를 정의합니다.
	
	private String studentName;	//학생이름
	private int grade; 			//학년
	private int money;			//학생이 가지고 있는 돈
	
	//학생이름과 가진 돈을 매개변수로 받는 생성자
	public Student(String studentName, int money) {
		
		this.studentName=studentName;
		this.money=money;
		
		
	}

	
	//학생이 버스를 타면 1,000원을 지불하는 기능을 구현한 매서드
	
	public void takeBus(Bus bus) {
		
		bus.take(1000);
		this.money-=1000;
		
		
	}
	//학생이 지하철를 타면 1,500원을 지불하는 기능을 구현한 매서드
	
		public void takeSubway(Subway subway) {
			
			subway.take(1500);
			this.money-=1500;
			
			
			
		}
		
		//학생의 현재 정보를 출력하는 매서드
		
		public void showInfo() {
						
			System.out.println("studentName"+"님의 남은 돈은"+money+"원입니다.");
			
		}
		
		
}
