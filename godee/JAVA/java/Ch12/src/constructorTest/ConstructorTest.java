//페키지 이름이 constructorTest입니다.
package constructorTest;

//Person 클래스를 정의합니다.
class Person{
	
	//필드(멤버변수)를 정의합니다.
	String name;	//이름
	int age;		//나이

	//디폴트 생성자를 정의합니다.
	Person(){
		//주석 해제하기 그럼 오류남
		//name="이름없음";
		//age=1;
		//System.out.println("Person 객체가 생성되었습니다.");
		
		
		//생성자 오버로딩
		//this 예약어를 사용해 Person(String,int) 생성자를 호출합니다.
		this("이름없음",1);
		System.out.println("Person 객체가 생성되었습니다.");
		
	}
	
	
	
	//이름과 나이를 부여하는 매개변수가 있는 생성자를 정의합니다.
	public Person(String name, int age) {
		
		this.name = name;
		this.age = age;

	}

	//자신의 주소를 반환하는 메서드를 정의합니다.
	
	Person returnItSelf() {
		
		
		return this;
	}

}



//클래스이름이 ConstructorTest입니다.
public class ConstructorTest {

	//main()메서드를 생성했습니다.
	public static void main(String[] args) {
		// TODO Auto-generated method stub

		
		
		//noPerson 객체를 생성합니다. ->디폴트생성자가 호출 됩니다.
		Person noPerson=new Person();
		
		
		//noPerson 객체의 이름을 출력합니다.
		System.out.println(noPerson.name);
		
		//noPerson 객체의 나이을 출력합니다.
		System.out.println(noPerson.age);
		
		//자신의 주소를 반환하는 메서드를 사용하여 noPerson객체와 person 클래스 변수의 주소를 출력하시오
		//this 값을 person 클래스 변수에 대입합니다.
		Person person = noPerson.returnItSelf();
		
		
		//noPerson.returnItSelf() 메서드의 반환값을 출력합니다.
		System.out.println(person);
	
		//noPerson 객체(참조 변수)를 출력합니다.
		System.out.println(noPerson);
		
		
	}

}
