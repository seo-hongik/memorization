//패키지 이름이 interfaceTest 입니다.
package interfaceTest;

//인터페이스는 접근제한자와 final이 필요가 없다. 

// Calc 인터페이스를 정의합니다. 
public interface Calc {

	//필드(멤버변수)를 선언합니다. 
	
	//인터페이스에서 선언한 변수는 컴파일 과서정에서 상수로 변환됩니다.
	//final 안붙여도됨
	double PI=3.14;
	int ERROR=-9999;
	
	//메서드(멤버함수)를 선언합니다. 
	//인터페이스에서 선언한 메서드는 컴파일 과정에서 추상 메서드로 변환됩니다. 
	int add(int num1, int num2);
	int substract(int num1, int num2);
	//메서드 이름은 multiply로 하여도 무방합니다.
	int times(int num1, int num2);
	int divide(int num1, int num2);
	
	//디폴트 메서드 
	default void description() {
		System.out.println("정수 계산기를 구현 합니다.");
		myMethod();
	}
	
	//정적메서드
	static int total(int[] arr)  {
		int total=0;
		
		for(int i: arr) {
			
			total+=i;
		}
		
		//private static 메서드를 호출합니다. 
		myStaticMethod();
		return total;
	}
	
	//private 메서드를 정의합니다. 
	private void myMethod(){
		System.out.println("private 메서드 입니다. ");
	}
	
	//private static 메서드를 정의합니다. 
	private static void myStaticMethod() {
		System.out.println("private static 메서드 입니다. ");
	}
	
	
	
	
	
}
