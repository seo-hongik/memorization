import java.util.ArrayList;

class StaticExam{
    //1 static 변수(class 변수)
    private static final int immutable;
    private static final ArrayList<Integer> idx = new ArrayList<>();
    private static int mutable;
    private int nonStatic_Var = 0;

    //2 static 초기화 블럭
    static{
        immutable = 100;
        mutable = 100;

        for(int i = 0 ; i < 12; ++i){
            idx.add(i);
        }
    }

    //3 static 메서드
    public static void showStaticVars(){
        System.out.println(immutable);
        System.out.println(idx);
        System.out.println(mutable);
        //System.out.println(nonStatic_Var); non static 변수에는 접근 할 수 없다.
    }

    //4 중첩 클래스
    public static class NestedClass {
        int value;

        public void getValue(){
            System.out.println("중첩 클래스가 실행됨");
            //this.value = nonStatic_Var; // 중첩 class에선 외부 class의 인스턴스 변수에 접근 할 수 없다.
            showStaticVars(); // 메서드는 접근 가능.
        }
    }

    public void useNestedClassMethod(){
        NestedClass nc = new NestedClass(); // 중첩 Class의 instance를 생성
        nc.getValue();
    }

}
public class StaticClass {
    public static void main(String args []){
        System.out.println(Math.PI);// static 변수 : Math.PI
        System.out.println(Math.pow(2, 4));// static 메서드 : Math.pow(), System.

        StaticExam se = new StaticExam();
        StaticExam.showStaticVars(); 

        se.useNestedClassMethod();

    }
}
 
 